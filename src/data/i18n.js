export function normalizeLanguage(language) {
  return UI_COPY[language] ? language : DEFAULT_LANGUAGE;
}

export function getCopy(language) {
  return UI_COPY[normalizeLanguage(language)];
}

export function getLocale(language) {
  return LANGUAGES.find((item) => item.key === normalizeLanguage(language))?.locale || "en-US";
}

export function getTranslatedTiers(language) {
  const copy = getCopy(language);
  return TIERS.map((tier) => ({
    ...tier,
    label: copy.packages[tier.key]?.shortLabel || tier.label,
  }));
}

export function getCommercialTiers(language) {
  const copy = getCopy(language);
  return [
    { key: "starter", className: "starter", minPrice: 1200, ...copy.packages.starter },
    { key: "advanced", className: "advanced", minPrice: 2000, ...copy.packages.advanced },
    { key: "premium", className: "premium", minPrice: 0, ...copy.packages.premium },
  ];
}

export function getTranslatedGroups(language) {
  const lang = normalizeLanguage(language);
  if (lang === "en") return GROUPS;
  return GROUPS.map((group) => ({
    ...group,
    label: FR_GROUPS[group.id] || group.label,
    features: group.features.map((feature) => ({
      ...feature,
      ...(FR_FEATURES[feature.id] || {}),
    })),
  }));
}

export function getPackageLabel(packageValue, language) {
  const copy = getCopy(language);
  const value = String(packageValue || "").toLowerCase();
  if (value.includes("premium")) return copy.packages.premium.shortLabel;
  if (value.includes("advanced")) return copy.packages.advanced.shortLabel;
  if (value.includes("starter")) return copy.packages.starter.shortLabel;
  if (value === "none" || value.includes("no package") || value.includes("aucun")) return copy.packages.none;
  return packageValue || copy.drawer.packageFallback;
}
